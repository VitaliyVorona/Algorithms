from collections import OrderedDict


def checkio(data):
    roman = OrderedDict()
    roman[1000] = "M"
    roman[900] = "CM"
    roman[500] = "D"
    roman[400] = "CD"
    roman[100] = "C"
    roman[90] = "XC"
    roman[50] = "L"
    roman[40] = "XL"
    roman[10] = "X"
    roman[9] = "IX"
    roman[5] = "V"
    roman[4] = "IV"
    roman[1] = "I"

    num = data

    def roman_num(num):
        for i in roman.keys():
            x, y = divmod(num, i)
            yield roman[i]*x
            num -= (i*x)
            if num > 0:
                roman_num(num)
            else:
                break

    return


